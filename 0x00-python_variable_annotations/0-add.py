#!/usr/bin/env python3

"""
This module provides a function to add two floating-point numbers.

The add function takes two parameters, both of type float,
and returns their sum as a float.
This module is designed to be imported and used in other Python scripts.
"""


def add(a: float, b: float) -> float:
    """
    Adds two floating-point numbers and returns the result.

    Parameters:
    a (float): The first number to add.
    b (float): The second number to add.

    Returns:
    float: The sum of a and b.
    """
    return a + b
